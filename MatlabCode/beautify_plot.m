set(gca, 'Color', [1 1 1]); % Sets axes background
set(gcf, 'Renderer', 'painters');  %
FontSize = 14;%11*1.5;
FontName = 'Helvetica';

%hXLabel = xlabel(figXLabel);
%hYLabel = ylabel(figYLabel);
